    public int getAccInfo(String operation){
        PreparedStatement st;
        ResultSet rs;
        int out = 0;
        
        String query = "SELECT * FROM users WHERE id>1";
        int total=0, saving=0, credit=0,checking=0;
        try{
            st = DerbyConn.getConnection().prepareStatement(query);
            rs = st.executeQuery();
            
            while(rs.next()){
                total++;
//                String accType = rs.getString("accType");
//                System.out.println(accType);
//                System.out.println();
                if((rs.getString("accType")).equals("Saving Account")){
                    saving++;
                }else if(rs.getString("accType").equals("Credit Account")){
                    credit++;
                }else{
                    checking++;
                }
            }
            switch(operation){
                case "total": out = total; break;
                case "saving": out = saving; break;
                case "credit": out = credit; break;
                case "checking": out = checking; break;
            }
        }catch (SQLException ex) {
//            Logger.getLogger(LoginWindow.class.getName()).log(Level.SEVERE, null, ex);
              System.out.println(ex);
              JOptionPane.showMessageDialog(null, ex,"Get Info Error",2);
        }
        
        return out;
    }
    
    public double getBalanceInfo(String operation){
        PreparedStatement st;
        ResultSet rs;
        double out = 0;
        
        String query = "SELECT * FROM users WHERE id>1";
        
        double totalBalance=0, totalAcc=0;
        double avg=0;

        try{
            st = DerbyConn.getConnection().prepareStatement(query);
            rs = st.executeQuery();
            
            while(rs.next()){
                totalAcc++;
                totalBalance += rs.getDouble("balance");
            }
            avg = totalBalance/totalAcc;
            switch(operation){
                case "total": out = totalBalance; break;
                case "avg": out = avg; break;
            }
        }catch (SQLException ex) {
//            Logger.getLogger(LoginWindow.class.getName()).log(Level.SEVERE, null, ex);
              System.out.println(ex);
              JOptionPane.showMessageDialog(null, ex,"Get Info Error",2);
        }
        
        return out;
    }

