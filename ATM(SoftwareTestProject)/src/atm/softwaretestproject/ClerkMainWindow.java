    private void RegBtnMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_RegBtnMouseClicked
        // TODO add your handling code here:
//        String accNumb = jTextField_AccNumb.getText();
        String pin = String.valueOf(jPasswordField1.getPassword());
        String confPin = String.valueOf(jPasswordField2.getPassword());
        String name = jTextField_Name.getText();
        String acc = jTextField_accType.getText();
        String deposit = jTextField_deposit.getText();
        
        if(pin.equals("")){
            JOptionPane.showMessageDialog(null, "PIN field cannot be empty!", "Empty Field", 2);
        }else if(!pin.matches("\\d{6}")){
            JOptionPane.showMessageDialog(null, "PIN must be a 6-digit number", "Wrong length", 2);
        }else if(confPin.equals("")){
            JOptionPane.showMessageDialog(null, "Confirm PIN field cannot be empty!", "Empty Field", 2);
        }else if(!confPin.equals(pin)){
            JOptionPane.showMessageDialog(null, "The PIN and Confirmation PIN do not match", "Wrong Confirmation PIN", 2);
        }else if(name.equals("")){
            JOptionPane.showMessageDialog(null, "Name cannot be empty", "Empty Field", 2);
        }else if(!name.matches("[A-Z a-z]+")){
            JOptionPane.showMessageDialog(null, "Name cannot have digits or symbol", "Warning", 2);
        }else if(acc.equals("")){
            String str = jLabel8.getText().concat(" field cannot be empty");
            JOptionPane.showMessageDialog(null, str, "Empty field", 2);
        }else if(!acc.matches("[0-9.]+")){
            String str = jLabel8.getText().concat(" field must be a number");
            JOptionPane.showMessageDialog(null, str, "Warning", 2);
        }else if(deposit.equals("")){
            JOptionPane.showMessageDialog(null, "Deposit field cannot be empty", "Empty field", 2);
        }else if(!deposit.matches("[0-9.]+")){
            JOptionPane.showMessageDialog(null, "Deposit field must be a number", "Empty field", 2);
        }else{
            String accType = jComboBox1.getSelectedItem().toString();
            Double balance = Double.parseDouble(deposit);
            Double interestRate=0.0, creditLine=0.0, transFee=0.0;
            if(accType.equals("Saving Account")){
                interestRate=Double.parseDouble(acc);
            }else if(accType.equals("Credit Account")){
                creditLine=Double.parseDouble(acc);
            }else{
                transFee=Double.parseDouble(acc);
            }
            createAcc(pin, name, accType, balance, interestRate, creditLine, transFee);
//            createAcc(String name, String accType, Integer balance, Double interestRate, Double creditLine, Double transFee)
            jTable1.setModel(new DefaultTableModel(null, new Object[]{"Account Number", "Name", "Balance", "Account Type", "Creation Date"}));
            AccSearchOperations.fillTable(jTable1, "", sortByArg());
            setFieldAccSummary();
        }
    }//GEN-LAST:event_RegBtnMouseClicked

