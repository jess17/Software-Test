    public String sortByArg(){
        String arg="";
        if(sortByComboBox.getSelectedItem()== "Account Number"){
            arg="accNumb";
        }else if(sortByComboBox.getSelectedItem()== "Name"){
            arg="name";
        }else if(sortByComboBox.getSelectedItem()== "Balance"){
            arg="balance";
        }else if(sortByComboBox.getSelectedItem()== "Account Type"){
            arg="accType";
        }else{
            arg="dateCreated";
        }
        return arg;
    }

