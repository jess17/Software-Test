    public String getInfo(String colName, int id){
//        String name=null;
        PreparedStatement st;
        ResultSet rs;
        String out = null;
        
        String query = "SELECT * FROM users WHERE id=?";
        
        try{
            st = DerbyConn.getConnection().prepareStatement(query);
            st.setInt(1, id);
            rs = st.executeQuery();
            
            
            while(rs.next()){
                switch(colName){
                    case "id": out = rs.getString("id"); break;
                    case "userType": out = rs.getString("userType"); break;
                    case "accNumb": out = rs.getString("accNumb"); break;
                    case "password": out = rs.getString("password"); break;
                    case "name": out = rs.getString("name"); break;
                    case "accType": out = rs.getString("accType"); break;
                    case "balance": out = rs.getString("balance"); break;
                    case "interestRate": out = rs.getString("InterestRate"); break;
                    case "creditLine": out = rs.getString("creditLine"); break;
                    case "transFee": out = rs.getString("transFee"); break;
                    case "dateCreated": out = rs.getString("dateCreated"); break;
                }
                        
                
            }
            
        }catch (SQLException ex) {
//            Logger.getLogger(LoginWindow.class.getName()).log(Level.SEVERE, null, ex);
              System.out.println(ex);
              JOptionPane.showMessageDialog(null, ex,"Get Info Error",2);
        }
        
        return out;
    }
